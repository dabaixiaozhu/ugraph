package com.winit.graph.entity.vo;

/**
 * 返回工程的版本路径
 */
public class VersionPathVO {

    private String appVersionName;

    private String appVersionPath;

    public String getAppVersionName() {
        return appVersionName;
    }

    public void setAppVersionName(String appVersionName) {
        this.appVersionName = appVersionName;
    }

    public String getAppVersionPath() {
        return appVersionPath;
    }

    public void setAppVersionPath(String appVersionPath) {
        this.appVersionPath = appVersionPath;
    }
}
